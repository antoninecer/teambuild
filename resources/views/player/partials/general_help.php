<div class="help-block">
    <h3>1. Co je cílem hry</h3>
    <p>
        Hra je terénní mapová výprava, ve které se hráč nebo tým pohybuje podle mapy,
        objevuje body zájmu, získává informace, hledá poklady a podle pravidel konkrétní hry
        sbírá body nebo plní postupové cíle.
    </p>
    <p>Každá hra může mít trochu jiný styl:</p>
    <ul>
        <li>průzkumný</li>
        <li>příběhový</li>
        <li>soutěžní</li>
        <li>městský</li>
        <li>týmový</li>
    </ul>
    <p>Proto vždy platí dvě vrstvy pravidel:</p>
    <ul>
        <li>obecné ovládání aplikace</li>
        <li>briefing konkrétní hry</li>
    </ul>
</div>

<div class="help-block">
    <h3>2. Co je potřeba před startem</h3>
    <p>Před zahájením hry si připrav:</p>
    <ul>
        <li>mobilní telefon s funkčním internetem</li>
        <li>povolenou polohu / GPS pro prohlížeč</li>
        <li>dostatečně nabitou baterii</li>
        <li>ideálně i nabitou powerbanku</li>
        <li>telefonní kontakt, který zadáš při registraci pro svou bezpečnost</li>
    </ul>
    <p>
        <strong>Doporučení:</strong><br>
        Mít s sebou nabitou powerbanku není od věci. U delších her nebo při slabším signálu
        se může telefon vybíjet rychleji.
    </p>
</div>

<div class="help-block">
    <h3>3. Registrace do hry</h3>
    <p>Při vstupu do hry zadáš:</p>
    <ul>
        <li>přezdívku</li>
        <li>případně další údaje podle nastavení hry</li>
        <li>svůj telefonní kontakt pro případ bezpečnostní nebo organizační potřeby</li>
    </ul>
    <p>Telefonní kontakt slouží zejména pro situace, kdy:</p>
    <ul>
        <li>potřebuješ pomoc</li>
        <li>ztratíš se</li>
        <li>organizátor tě potřebuje rychle kontaktovat</li>
        <li>dojde k provozní nebo bezpečnostní komplikaci</li>
    </ul>
</div>

<div class="help-block">
    <h3>4. Jak hra funguje</h3>
    <p>Po vstupu do hry uvidíš mapu a svoji polohu.</p>
    <p>
        Během hry se pohybuješ v terénu. Když se přiblížíš k zajímavému místu,
        hra ti nabídne možnost průzkumu.
    </p>
    <p>Typická herní smyčka je:</p>
    <ol>
        <li>dojdi na místo</li>
        <li>přibliž se do oblasti</li>
        <li>použij akci <strong>Prozkoumat okolí</strong></li>
        <li>objev POI nebo poklad</li>
        <li>přečti si nebo si nech přehrát obsah</li>
        <li>potvrď průzkum nebo seber poklad</li>
        <li>pokračuj dál</li>
    </ol>
</div>

<div class="help-block">
    <h3>5. Co znamená „Prozkoumat okolí“</h3>
    <p>
        Tlačítko <strong>Prozkoumat okolí</strong> je hlavní herní akce.
    </p>
    <p>
        Použij ho tehdy, když jsi na místě nebo poblíž místa, které má být prozkoumáno.
    </p>
    <p>
        <strong>Důležité:</strong><br>
        Za návštěvu se nepočítá pouhý průchod kolem. Hra počítá skutečný průzkum až ve chvíli,
        kdy s místem aktivně interaguješ.
    </p>
    <p>To znamená:</p>
    <ul>
        <li>nestačí kolem jen projet</li>
        <li>je potřeba zastavit</li>
        <li>otevřít nebo potvrdit interakci</li>
    </ul>
</div>

<div class="help-block">
    <h3>6. Co je POI</h3>
    <p>POI je bod zájmu.</p>
    <p>Může obsahovat:</p>
    <ul>
        <li>text</li>
        <li>příběh</li>
        <li>obrázek</li>
        <li>video</li>
        <li>zvuk</li>
        <li>stopu</li>
        <li>herní informaci</li>
    </ul>
    <p>POI může:</p>
    <ul>
        <li>pouze předat informaci</li>
        <li>navést tě na další místo</li>
        <li>odemknout poklad</li>
        <li>být součástí příběhu nebo úkolu</li>
    </ul>
</div>

<div class="help-block">
    <h3>7. Co je poklad</h3>
    <p>Poklad je herní odměna nebo speciální nález.</p>
    <p>Poklad může být:</p>
    <ul>
        <li>viditelný přímo</li>
        <li>schovaný a objevený až průzkumem</li>
        <li>odemčený až po splnění POI</li>
        <li>určený jednotlivci nebo týmu</li>
    </ul>
    <p>Když poklad najdeš, hra ti nabídne možnost jeho sebrání.</p>
</div>

<div class="help-block">
    <h3>8. Kdy se něco započítá</h3>
    <p>Za splněné se počítá až:</p>
    <ul>
        <li>potvrzený průzkum POI</li>
        <li>sebraný poklad</li>
        <li>nebo jiná explicitní interakce podle pravidel hry</li>
    </ul>
    <p>Nestačí jen být poblíž.</p>
</div>

<div class="help-block">
    <h3>9. Když hra nic nenajde</h3>
    <p>
        Jestliže jsi přesvědčený, že jsi na správném místě, a hra po stisknutí
        <strong>Prozkoumat okolí</strong> nic nenajde:
    </p>
    <ul>
        <li>zkontroluj, že máš povolenou polohu</li>
        <li>počkej několik sekund na zpřesnění GPS</li>
        <li>popojdi o pár metrů</li>
        <li>zkus akci znovu</li>
        <li>nezkoušej to jen za chůze, na chvíli se zastav</li>
    </ul>
    <p>
        Uvnitř budov, v husté zástavbě nebo při horším signálu může být poloha nepřesná.
    </p>
</div>

<div class="help-block">
    <h3>10. SOS a pomoc</h3>
    <p>Pokud máš problém, použij SOS.</p>
    <p>Použij ho zejména když:</p>
    <ul>
        <li>ses ztratil</li>
        <li>nevíš, co dál</li>
        <li>máš technický problém</li>
        <li>došla ti baterie nebo se vybíjí telefon</li>
        <li>potřebuješ organizační nebo zdravotní pomoc</li>
    </ul>
    <p>Do zprávy napiš stručně a jasně:</p>
    <ul>
        <li>co se stalo</li>
        <li>kde přibližně jsi</li>
        <li>jestli můžeš pokračovat</li>
        <li>jestli potřebuješ okamžitou pomoc</li>
    </ul>
    <p>
        <strong>Příklad:</strong><br>
        „Jsem na Karláku u kostela, telefon má 5 %, nevím kam dál.“
    </p>
</div>

<div class="help-block">
    <h3>11. Co dělat při slabé baterii</h3>
    <p>Když ti dochází baterie:</p>
    <ul>
        <li>omez zbytečné aplikace na pozadí</li>
        <li>stáhni jas displeje</li>
        <li>drž otevřenou jen potřebnou stránku hry</li>
        <li>připoj powerbanku</li>
        <li>případně včas informuj organizátora přes SOS</li>
    </ul>
</div>

<div class="help-block">
    <h3>12. Bezpečnostní doporučení</h3>
    <p>Během hry:</p>
    <ul>
        <li>sleduj provoz kolem sebe</li>
        <li>nevbíhej do vozovky</li>
        <li>nevstupuj na nebezpečná místa</li>
        <li>respektuj soukromý majetek</li>
        <li>nenech se strhnout soutěží na úkor bezpečnosti</li>
    </ul>
    <p>Bezpečnost má přednost před body i tempem hry.</p>
</div>

<div class="help-block">
    <h3>13. Briefing konkrétní hry</h3>
    <p>Každá hra může mít vlastní:</p>
    <ul>
        <li>cíl</li>
        <li>délku</li>
        <li>bodování</li>
        <li>speciální pravidla</li>
        <li>doporučený postup</li>
        <li>tematické zasazení</li>
    </ul>
    <p>
        Před startem si vždy přečti briefing konkrétní hry, protože ten určuje,
        jak se má hrát právě tato konkrétní varianta.
    </p>
</div>

<div class="help-block">
    <h3>14. Stručná verze pro zapamatování</h3>
    <ul>
        <li>zapni polohu</li>
        <li>měj nabitý telefon</li>
        <li>ideálně vezmi powerbanku</li>
        <li>při registraci uveď telefonní kontakt</li>
        <li>chodíš podle mapy</li>
        <li>na místě použij „Prozkoumat okolí“</li>
        <li>POI čti nebo poslouchej</li>
        <li>poklady sbírej až po nalezení</li>
        <li>při problému použij SOS</li>
    </ul>
</div>